package Modal;

public class FarmersProducts {
private String fid;
private String fname;
private String fProduct;
private String amountOfSeeds;
private String harvestTime;
private String harvestLand;
public String getFid() {
	return fid;
}
public void setFid(String id) {
	this.fid = id;
}
public String getFname() {
	return fname;
}
public void setFname(String fname) {
	this.fname = fname;
}
public String getfProduct() {
	return fProduct;
}
public void setfProduct(String fProduct) {
	this.fProduct = fProduct;
}
public String getAmountOfSeeds() {
	return amountOfSeeds;
}
public void setAmountOfSeeds(String amountOfSeeds) {
	this.amountOfSeeds = amountOfSeeds;
}
public String getHarvestTime() {
	return harvestTime;
}
public void setHarvestTime(String harvestTime) {
	this.harvestTime = harvestTime;
}
public String getHarvestLand() {
	return harvestLand;
}
public void setHarvestLand(String harvestLand) {
	this.harvestLand = harvestLand;
}


}
